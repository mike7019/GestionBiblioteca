    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        String titulo = txt_titulo.getText();

        if (titulo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el t√≠tulo del libro", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            LibroDAO libroDAO = new LibroDAO();
            libroActual = libroDAO.obtenerLibroPorTitulo(titulo);

            if (libroActual != null) {
                txt_autor.setText(libroActual.getAutor());
                cbx_categoria.setSelectedItem(libroActual.getCategoria());
                cbx_estado.setSelectedItem(libroActual.getEstado());

                txt_autor.setEnabled(true);
                cbx_categoria.setEnabled(true);
                cbx_estado.setEnabled(true);
                btn_actualizar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ un libro con ese t√≠tulo", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar el libro: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_consultarActionPerformed

    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        String nuevoAutor = txt_autor.getText();
        String nuevaCategoria = (String) cbx_categoria.getSelectedItem();
        String nuevoEstado = (String) cbx_estado.getSelectedItem();

        // Validar los campos editados
        if (nuevoAutor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Actualizar el objeto libroActual
        libroActual.setAutor(nuevoAutor);
        libroActual.setCategoria(nuevaCategoria);
        libroActual.setEstado(nuevoEstado);

        // Actualizar el libro en la base de datos
        try {
            LibroDAO libroDAO = new LibroDAO();
            libroDAO.editarLibro(libroActual);  // M√©todo de actualizaci√≥n en el DAO

            JOptionPane.showMessageDialog(this, "Libro actualizado exitosamente");

            // Volver al Dashboard y refrescar la tabla
            dashboard.mostrarLibros();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar el libro: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }         // TODO add your handling code here:
    }//GEN-LAST:event_btn_actualizarActionPerformed

