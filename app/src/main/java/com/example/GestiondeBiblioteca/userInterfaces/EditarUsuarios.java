    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        String nuevoNombres = txt_nombres.getText();
        String nuevaContrasena = txt_password.getText();
        String nuevoRol = (String) cbx_rol.getSelectedItem();
        String nombreUsuario = txt_usuario.getText();

        // Validar los campos editados
        if (nombreUsuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Actualizar el objeto libroActual
        usuarioActual.setNombre(nuevoNombres);
        usuarioActual.setRol(nuevoRol);
        usuarioActual.setContrasena(nuevaContrasena);

        // Actualizar el libro en la base de datos
        try {
            UsuarioDAO usuarioDAO = new UsuarioDAO();
            usuarioDAO.actualizarUsuario(usuarioActual);  // M√©todo de actualizaci√≥n en el DAO

            JOptionPane.showMessageDialog(this, "Usuario actualizado exitosamente");

            // Volver al Dashboard y refrescar la tabla
            dashboard.mostrarUsuarios();
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }         // TODO add your handling code here:
    }//GEN-LAST:event_btn_actualizarActionPerformed

    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        String usuario = txt_usuario.getText();

        if (usuario.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese el usuario a consultar...", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            UsuarioDAO usuarioDAO = new UsuarioDAO();
            usuarioActual = usuarioDAO.obtenerUsuarioPorNombreUsuario(usuario);

            if (usuarioActual != null) {
                txt_nombres.setText(usuarioActual.getNombre());
                cbx_rol.setSelectedItem(usuarioActual.getRol());
                txt_password.setText(usuarioActual.getContrasena());

                txt_nombres.setEnabled(true);
                cbx_rol.setEnabled(true);
                txt_password.setEnabled(true);
                btn_actualizar.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ un usuario con el nombre dado", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
            dashboard.mostrarUsuarios();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al buscar el usuario: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_consultarActionPerformed

